package com.company;

public class java22_irregularArray {
    public static void main(String[] args) {
        int table [][] = new int[3][];
        table[0] = new int [4] ;//
        table[1] = new int[4];
        table[2] = new int [4];

        for(int i = 0; i<table.length ; i++){
            for(int j = 0; i< table.length ; j++){
                System.out.println(table[i][j]);                                                                                                                                                                                                                                                                                                                                                                               
            }
        }
    } 
     
    
}

