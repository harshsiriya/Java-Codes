public class java05_operators {
    public static void main(String[] args) {
        int a = 6;
        int b = a+ 4;  //  ariithematic operator
        System.out.println(b);
        b += 10;  //  assignment operator
        System.out.println(b);
        System.out.println(a==b);  //  comparision operator
        System.out.println(a!=b && a<b);  //  logical operator


        
    }
    
}
